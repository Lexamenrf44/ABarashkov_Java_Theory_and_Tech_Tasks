package theory.examples.utility_classes;

/*

Math

Provides static methods for performing basic mathematical operations, such as Math.max(), Math.min(), Math.pow(), Math.sqrt(), and more.

*/

public class Math {
    public static void main(String[] args) {
        // Compute the power of a number
        double result = java.lang.Math.pow(2, 3);  // 2 raised to the power of 3

        // Print the result
        System.out.println(result);  // Output: 8.0

        // Compute the square root of a number
        System.out.println(java.lang.Math.sqrt(16));  // Output: 4.0
    }
}
